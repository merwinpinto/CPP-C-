#include<iostream>
using namespace std;
int main()
{
    cout <<"Displaying different  Data type sizes in C++"<<endl;
    cout<<endl;
    cout << "using sizeof(data type) operator"<<endl;
    cout<<endl;

    cout << "Data type chart !"<<endl;
    cout <<endl;
    cout << "   DATA TYPE    No of Bytes "<<endl;
    cout << "   char           " << sizeof(char) << endl;
    cout << "   int            " << sizeof(int) << endl;
    cout << "   unsigned int   " << sizeof(unsigned int) << endl;
    cout << "   float          " << sizeof(float) <<endl;
    cout << "   double         " << sizeof(double) <<endl;

}
